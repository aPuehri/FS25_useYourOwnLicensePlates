----------------------------------------------------------------------------------------------------------------------------								
Deutsch:								
----------------------------------------------------------------------------------------------------------------------------
beim ersten Start mit dem Mod, wird in den "modSettings" ein Ordner FS25_useYourOwnLicensePlates erstellt
	--> C:\Users\xxxx\Documents\My Games\FarmingSimulator2025\modSettings\FS25_useYourOwnLicensePlates
und dieses Hilfefile kopiert!

In diesem Ordner befinden sich zusätzlich noch folgende Dateien:
	- licensePlatesCustom.i3d
	- licensePlatesCustom.i3d.shapes
	- licensePlatesCustom.xml
	- licensePlatesCustom_diffuse.dds
	
WICHTIG: Die Dateinamen dürfen nicht verändert werden!!!!

Um nun deine eigenen Kennzeichen zu nutzen hast du folgende Möglichkeiten:
	a) editieren der oben genannten Dateien nach deinen eigenen Vorstellungen 
			(BackUp nicht vergessen die Dateien werden nicht durch den Mod gesichert)
			
	b) verwenden der bereits im Mod hinterlegten Kennzeichen
			dazu wie folgt vorgehen:
			
			1. im Mod findest du den Ordner \licensePlates
				darin befinden sich die vorbereiteten Kennzeichen
				\licensePlates
				  |_ AUT (Österreich)
					 |_B 	(Burgenland)
					 |_K 	(Kärnten)
					 |_NOE 	(Niederösterreich)
					 |_OOE 	(Oberösterreich)
					 |_S 	(Salzburg)
					 |_ST 	(Steiermark)
					 |_T 	(Tirol)
					 |_V 	(Vorarlberg)
					 |_W 	(Wien)
				  |_ GER (Deutschland)
				  
			2. um ein vorgefertigtes Kennzeichen zu verwenden musst du nur die 4 Dateien aus einem der oben 
			   angeführten Verzeichnissein im Mod kopieren
			   und im \modSettings\FS25_useYourOwnLicensePlates einfügen (vorhandene Dateien überschreiben)
			   
			3. wenn das Spiel neu gestartet wird, werden die Kennzeichen aus dem \modSettings\FS25_useYourOwnLicensePlates
			   geladen
			   
			   
Solltest du beim anpassen oder bearbeiten mal einen Fehler gemacht haben, oder du dir nicht sicher bist ob wirklich alle Dateien passen,
dann kannst du einfach den FS25_useYourOwnLicensePlates Ordner in den "modSettigs" löschen.
Der Ordner wird beim erneuten Spielstart wieder erstellt und mit der Vorlage aus dem Mod befüllt!
			   
Viel Spaß!!!!

