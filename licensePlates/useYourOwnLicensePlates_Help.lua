English:
----------------------------------------------------------------------------------------------------------------------------
When you start the mod for the first time, a folder FS25_useYourOwnLicensePlates is created in the "modSettings"
	--> C:\Users\xxxx\Documents\My Games\FarmingSimulator2025\modSettings\FS25_useYourOwnLicensePlates
and this help file is copied!

This folder also contains the following files:
	- licensePlatesCustom.i3d
	- licensePlatesCustom.i3d.shapes
	- licensePlatesCustom.xml
	- licensePlatesCustom_diffuse.dds

IMPORTANT: The file names must not be changed!!!!

To use your own license plates you have the following options:

	a) edit the above files according to your own ideas
			(don't forget to back up the files, the files are not backed up by the mod)

	b) use the license plates already stored in the mod
			to do this proceed as follows:

			1. in the mod you will find the folder \licensePlates
				in this you will find the prepared license plates
				\licensePlates
				  |_ AUT (Austria)
					|_B (Burgenland)
					|_K (Carinthia)
					|_NOE (Lower Austria)
					|_OOE (Upper Austria)
					|_S (Salzburg)
					|_ST (Styria)
					|_T (Tyrol)
					|_V (Vorarlberg)
					|_W (Vienna)
				  |_ GER (Germany)

			2. to use a pre-made license plate you just have to copy the 4 files from one of the above
			directories into the mod
			and in the Insert \modSettings\FS25_useYourOwnLicensePlates (overwrite existing files)

			3. When the game is restarted, the license plates from \modSettings\FS25_useYourOwnLicensePlates

			If you make a mistake when adjusting or editing, or you are not sure whether all the files really fit,
			then you can simply delete the FS25_useYourOwnLicensePlates folder in the "modSettings".
			The folder will be created again when the game is restarted and filled with the template from the mod!

Have fun!!!!
